/**
 * Contexto para gestionar el idioma de la aplicaci√≥n (es-ES/ca-ES).
 * Sincroniza con Supabase user_settings y localStorage.
 */
export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = React.useState<LanguageCode>(() => {
    const stored = localStorage.getItem('language') as LanguageCode | null;
    return stored ?? 'ca-ES'; // Idioma por defecto: CATAL√ÅN
  });

  /**
   * Funci√≥n de traducci√≥n con soporte para interpolaci√≥n de variables.
   *
   * Soporta variables en formato {{variableName}} que se reemplazan con los valores
   * proporcionados en el objeto de par√°metros.
   *
   * @param {string} key - Clave de traducci√≥n
   * @param {Record<string, string | number>} [params] - Par√°metros para interpolaci√≥n
   * @returns {string} Texto traducido con variables interpoladas
   * @example
   * t("actions.confirmDelete", { productName: "Producto 1" })
   * // "¬øEst√°s seguro de eliminar el producto \"Producto 1\"?"
   */
  const t = React.useCallback(
    (key: string, params?: Record<string, string | number>): string => {
      let text = translations[language]?.[key] ?? key;

      // Interpolar variables si se proporcionan par√°metros
      if (params) {
        Object.keys(params).forEach((paramKey) => {
          const value = String(params[paramKey]);
          // Reemplazar {{paramKey}} con el valor
          text = text.replace(new RegExp(`\\{\\{${paramKey}\\}\\}`, 'g'), value);
        });
      }

      return text;
    },
    [language],
  );

  // Sincronizar idioma con Supabase cuando cambia
  const setLanguage = React.useCallback(async (lang: LanguageCode) => {
    setLanguageState(lang);
    localStorage.setItem('language', lang);

    // Sincronizar con Supabase si hay sesi√≥n activa
    try {
      const { supabaseClient } = await import('@infrastructure/supabase/supabaseClient');
      const {
        data: { session },
      } = await supabaseClient.auth.getSession();

      if (session?.user?.id) {
        await supabaseClient.from('user_settings').upsert(
          {
            user_id: session.user.id,
            language: lang,
            updated_at: new Date().toISOString(),
          },
          {
            onConflict: 'user_id',
          },
        );
      }
    } catch (error) {
      // eslint-disable-next-line no-console
      console.warn('[LanguageContext] Error sincronizando idioma con Supabase:', error);
    }
  }, []);

  // Cargar idioma desde Supabase al montar si hay sesi√≥n
  React.useEffect(() => {
    const loadLanguageFromSupabase = async () => {
      try {
        const { supabaseClient } =
          await import('@infrastructure/supabase/supabaseClient');
        const {
          data: { session },
        } = await supabaseClient.auth.getSession();

        if (session?.user?.id) {
          const { data: settings } = await supabaseClient
            .from('user_settings')
            .select('language')
            .eq('user_id', session.user.id)
            .single();

          if (
            settings?.language &&
            (settings.language === 'es-ES' || settings.language === 'ca-ES')
          ) {
            setLanguageState(settings.language as LanguageCode);
            localStorage.setItem('language', settings.language);
          }
        }
      } catch (error) {
        // eslint-disable-next-line no-console
        console.warn('[LanguageContext] Error cargando idioma desde Supabase:', error);
      }
    };

    loadLanguageFromSupabase();

    // Escuchar eventos de cambio de idioma desde AuthContext
    const handleLanguageChange = (event: CustomEvent) => {
      const newLanguage = event.detail as LanguageCode;
      if (newLanguage === 'es-ES' || newLanguage === 'ca-ES') {
        setLanguageState(newLanguage);
      }
    };

    window.addEventListener('language-changed', handleLanguageChange as EventListener);
    return () => {
      window.removeEventListener(
        'language-changed',
        handleLanguageChange as EventListener,
      );
    };
  }, []);

  // Actualizar el atributo 'lang' del HTML para ayudar al navegador
  React.useEffect(() => {
    document.documentElement.lang = language.split('-')[0];
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

/**
 * Hook para acceder al contexto de idioma.
 */
export function useLanguage() {
  const context = React.useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage debe usarse dentro de LanguageProvider');
  }
  return context;
}
